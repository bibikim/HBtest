package ex03_method;

public class Calculator {
	
	int add(int a, int b) {
		int result = a+b;
		return result;
	}

	int sub(int a, int b) {
		int result = a-b;
		return result;
	}
	
	int mul(int a, int b) {
		int result = a*b;
		return result;
	}
	
	double div(int a, int b) {
		double result = (double)a/b;
		return result;
	}
	
	
	
}
